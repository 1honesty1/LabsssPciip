<?php
// Создаём строковые переменные s1 и s2
$s1 = "Я люблю Гродно";
$s2 = "Я учусь в Политехническом колледже";

echo "Строка s1: $s1<br>";
echo "Строка s2: $s2<br><br>";

// 5.1 Определяем длину строки s2 (учитываем многобайтовую кодировку UTF-8)
$lengthS2 = mb_strlen($s2, 'UTF-8');
echo "Длина строки s2: $lengthS2<br><br>";

// 5.2 Выделяем n-ый символ в строке s1, где n = 25 (номер варианта)
$n = 25;
$lengthS1 = mb_strlen($s1, 'UTF-8');
if ($n > $lengthS1) {
    echo "В строке s1 нет $n-го символа (длина строки: $lengthS1).<br><br>";
} else {
    // Получаем n-ый символ (учитывая, что mb_substr считает от 0)
    $char = mb_substr($s1, $n - 1, 1, 'UTF-8');
    // Так как символы кириллицы в UTF-8 занимают несколько байт,
    // преобразуем символ в кодировку Windows-1251 для получения числового значения
    $charConverted = iconv('UTF-8', 'Windows-1251', $char);
    $asciiCode = ord($charConverted);
    echo "$n-ый символ строки s1: '$char', его ASCII-код (в кодировке Windows-1251): $asciiCode<br><br>";
}

// 5.3 Замена в строке s1 слова "Гродно" на "Минск"
$s1_replaced = str_replace("Гродно", "Минск", $s1);
echo "Строка s1 после замены: $s1_replaced<br>";
?> 