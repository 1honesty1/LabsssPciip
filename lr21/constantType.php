<?php
// Создаем константу NUM_E с значением 2.71828
define("NUM_E", 2.71828);

// Выводим значение константы
echo "Число e равно " . NUM_E . "<br>";

// Присваиваем значение константы переменной
$num_e1 = NUM_E;
echo "Исходный тип переменной \$num_e1: " . gettype($num_e1) . "<br>";

// Изменяем тип переменной на строковый
$num_e1 = (string)$num_e1;
echo "После приведения к строке, тип: " . gettype($num_e1) . ", значение: " . $num_e1 . "<br>";

// Изменяем тип переменной на целочисленный
$num_e1 = (int)$num_e1;
echo "После приведения к целому, тип: " . gettype($num_e1) . ", значение: " . $num_e1 . "<br>";

// Изменяем тип переменной на булевский
$num_e1 = (bool)$num_e1;
echo "После приведения к булевому, тип: " . gettype($num_e1) . ", значение: " . ($num_e1 ? "true" : "false") . "<br>";
?>
